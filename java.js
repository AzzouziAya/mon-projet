function displayPosters(list) {
  container.innerHTML = "";
  list.forEach(poster => {
    container.innerHTML += `
      <div class="post">
        <img src="${poster.image}" alt="${poster.name}" />
        <h3>${poster.name}</h3>
        <p>ðŸ’° Only ${poster.price}!</p>
      </div>
    `;
  });
}

displayPosters(posters);

